import math

#find the ceil of 2.3
a = 2.3
result = math.ceil(a)
print(result,"\n")

#find the floor of 2,3
a = 2.3
result = math.floor(a)
print(result,"\n")

#find the factorial of 5
a = 5 
result = math.factorial(a)
print(result,"\n")

#find the value of 2**3
a = 2**3
result = a
print(result,"\n")

#find the square root of 16
a = 16
result = math.sqrt(a)
print(result,"\n")