#asks to enter the first and second number
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

#formula
sum = num1 + num2
diff = num1 - num2
product = num1 * num2
quotient = num1 / num2
remainder = num1 % num2

#answers for the two numbers; sum, diff, product, quotient, remainder
print('The sum of ', num1, ' and ', num2, ' is: ', sum)
print('The difference of ', num1, ' and ', num2, ' is: ', diff)
print('The product of ', num1, ' and ', num2, ' is: ', product)
print('The quotient of ', num1, ' and ', num2, ' is: ', quotient)
print('The remainder of ', num1, ' and ', num2, ' is: ', remainder)